$("[data-callLang]").on("click", function () {
	if ($(this).attr("data-callLang") === "eng") {
		window.location.href = window.location.toString().split(/\?|#/)[0];
		return false;
	}

	window.location.hash = $(this).attr("data-callLang");
	location.reload(true);
});

